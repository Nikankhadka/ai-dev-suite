#!/usr/bin/env bash
# Regenerates the Claude Code and Codex CLI agent adapters and (re)creates
# every cross-harness symlink documented in README.md. Idempotent - safe to
# rerun any time.
#
# Verified against current docs before writing this script:
# - Claude Code: subagents in ~/.claude/agents/*.md need `name` + `description`;
#   `tools` is a comma list (omit to inherit all tools). Skill directories under
#   ~/.claude/skills/<name> may be symlinks to elsewhere on disk - Claude Code
#   follows them. Legacy ~/.claude/commands/*.md files still work; only
#   `description` in their frontmatter is meaningful to Claude Code.
# - Codex CLI: ~/.codex/AGENTS.md is merged with project AGENTS.md files, not
#   just read standalone. ~/.codex/prompts/*.md (deprecated but functional)
#   supports a `description` field. Codex's real global skills path is
#   ~/.agents/skills/<name>/SKILL.md (NOT ~/.codex/skills) and it follows
#   symlinked skill directories. Codex shipped custom subagents to GA in
#   March 2026: TOML files under ~/.codex/agents/*.toml with `name`,
#   `description`, `developer_instructions`, and an optional `sandbox_mode`
#   (`read-only`, `workspace-write`, `danger-full-access`; inherits from the
#   parent session if omitted). Unlike Claude Code, Codex does not auto-delegate
#   by description match - dispatch has to be requested explicitly, which is
#   why instructions/AGENTS.md carries a "When to dispatch a subagent" section.
set -euo pipefail

HUB="$(cd "$(dirname "${BASH_SOURCE[0]}")/.." && pwd)"
CLAUDE_DIR="$HOME/.claude"
CODEX_DIR="$HOME/.codex"
AGENTS_SKILLS_DIR="$HOME/.agents/skills"
SKILLS="lavish stack-discovery testing"
SHARED_AGENTS="planner reviewer tester maintainer"

status=0

link() {
  local target="$1" link_path="$2"
  mkdir -p "$(dirname "$link_path")"
  if [ -L "$link_path" ]; then
    if [ "$(readlink "$link_path")" = "$target" ]; then
      echo "ok:      $link_path -> $target"
      return 0
    fi
    rm "$link_path"
  elif [ -e "$link_path" ]; then
    local backup="${link_path}.pre-sync-adapters.bak"
    if [ -e "$backup" ]; then
      echo "WARN:    $link_path exists (not our symlink) and $backup already exists - resolve manually" >&2
      status=1
      return 0
    fi
    mv "$link_path" "$backup"
    echo "backed up: $link_path -> $backup"
  fi
  ln -s "$target" "$link_path"
  echo "linked:  $link_path -> $target"
}

# Single source of truth for each shared agent's tool grant, translated per
# harness below. Both gen_claude_agent and gen_codex_agent read from here so
# a grant only ever has to change in one place.
claude_tools_for() {
  case "$1" in
    planner) echo "Read, Grep, Glob, Bash" ;;
    *) echo "" ;;
  esac
}

codex_sandbox_for() {
  case "$1" in
    planner) echo "read-only" ;;
    *) echo "workspace-write" ;;
  esac
}

agent_description() {
  sed -n 's/^description: //p' "$HUB/agents/$1.md" | head -1
}

agent_body() {
  awk 'BEGIN{n=0} /^---$/{n++; next} n>=2{print}' "$HUB/agents/$1.md"
}

gen_claude_agent() {
  local name="$1"
  local tools description body dest
  tools="$(claude_tools_for "$name")"
  description="$(agent_description "$name")"
  body="$(agent_body "$name")"
  dest="$HUB/adapters/claude/agents/$name.md"
  {
    echo "---"
    echo "name: $name"
    echo "description: $description"
    if [ -n "$tools" ]; then
      echo "tools: $tools"
    fi
    echo "---"
    echo
    echo "<!-- GENERATED by bin/sync-adapters.sh from agents/$name.md - edit the source, not this file -->"
    printf '%s\n' "$body"
  } > "$dest"
  echo "generated: $dest"
}

gen_codex_agent() {
  local name="$1"
  local sandbox description body dest
  sandbox="$(codex_sandbox_for "$name")"
  description="$(agent_description "$name")"
  body="$(agent_body "$name")"
  dest="$HUB/adapters/codex/agents/$name.toml"
  # Escape backslashes and double quotes for TOML basic-string fields.
  description="${description//\\/\\\\}"
  description="${description//\"/\\\"}"
  {
    echo "# GENERATED by bin/sync-adapters.sh from agents/$name.md - edit the source, not this file"
    echo "name = \"$name\""
    echo "description = \"$description\""
    echo "sandbox_mode = \"$sandbox\""
    echo
    echo 'developer_instructions = """'
    printf '%s\n' "$body"
    echo '"""'
  } > "$dest"
  echo "generated: $dest"
}

echo "== Generating Claude Code agent adapters =="
mkdir -p "$HUB/adapters/claude/agents"
for a in $SHARED_AGENTS; do
  gen_claude_agent "$a"
done

echo
echo "== Generating Codex CLI agent adapters =="
mkdir -p "$HUB/adapters/codex/agents"
for a in $SHARED_AGENTS; do
  gen_codex_agent "$a"
done
# build.md is OpenCode's primary agent, not a subagent - no Claude Code or
# Codex adapter exists for it.

echo
echo "== Cleaning stale skill symlinks =="
stale_skills="coding-standards strategic-compact"
for s in $stale_skills; do
  for d in "$CLAUDE_DIR/skills/$s" "$AGENTS_SKILLS_DIR/$s"; do
    if [ -L "$d" ]; then
      rm "$d"
      echo "removed:  $d (stale - skill no longer in SKILLS)"
    fi
  done
done

echo
echo "== Wiring Claude Code (~/.claude) =="
link "$HUB/instructions/AGENTS.md" "$CLAUDE_DIR/CLAUDE.md"
link "$HUB/commands" "$CLAUDE_DIR/commands"
link "$HUB/adapters/claude/agents" "$CLAUDE_DIR/agents"
for s in $SKILLS; do
  link "$HUB/skills/$s" "$CLAUDE_DIR/skills/$s"
done

echo
echo "== Wiring Codex CLI (~/.codex, ~/.agents) =="
link "$HUB/instructions/AGENTS.md" "$CODEX_DIR/AGENTS.md"
link "$HUB/commands" "$CODEX_DIR/prompts"
link "$HUB/adapters/codex/agents" "$CODEX_DIR/agents"
for s in $SKILLS; do
  link "$HUB/skills/$s" "$AGENTS_SKILLS_DIR/$s"
done

echo
echo "== Verifying =="
broken=0
for d in "$CLAUDE_DIR" "$CODEX_DIR" "$AGENTS_SKILLS_DIR"; do
  [ -d "$d" ] || continue
  while IFS= read -r -d '' link_path; do
    if [ ! -e "$link_path" ]; then
      echo "BROKEN: $link_path" >&2
      broken=1
    fi
  done < <(find "$d" -maxdepth 3 -type l -print0 2>/dev/null)
done

if [ "$broken" -eq 1 ]; then
  echo "sync-adapters: one or more symlinks are broken" >&2
  exit 1
fi
if [ "$status" -ne 0 ]; then
  echo "sync-adapters: completed with warnings - see above" >&2
  exit 1
fi
echo "sync-adapters: OK"
