#!/usr/bin/env bash
# Regenerates Claude Code agent adapters and (re)creates every cross-harness
# symlink documented in README.md. Idempotent - safe to rerun any time.
#
# Verified against current docs before writing this script:
# - Claude Code: subagents in ~/.claude/agents/*.md need `name` + `description`;
#   `tools` is a comma list (omit to inherit all tools). Skill directories under
#   ~/.claude/skills/<name> may be symlinks to elsewhere on disk - Claude Code
#   follows them. Legacy ~/.claude/commands/*.md files still work; only
#   `description` in their frontmatter is meaningful to Claude Code.
# - Codex CLI: ~/.codex/AGENTS.md is merged with project AGENTS.md files, not
#   just read standalone. ~/.codex/prompts/*.md (deprecated but functional)
#   supports a `description` field. Codex's real global skills path is
#   ~/.agents/skills/<name>/SKILL.md (NOT ~/.codex/skills) and it follows
#   symlinked skill directories.
set -euo pipefail

HUB="$(cd "$(dirname "${BASH_SOURCE[0]}")/.." && pwd)"
CLAUDE_DIR="$HOME/.claude"
CODEX_DIR="$HOME/.codex"
AGENTS_SKILLS_DIR="$HOME/.agents/skills"
SKILLS="coding-standards lavish testing stack-discovery strategic-compact"

status=0

link() {
  local target="$1" link_path="$2"
  mkdir -p "$(dirname "$link_path")"
  if [ -L "$link_path" ]; then
    if [ "$(readlink "$link_path")" = "$target" ]; then
      echo "ok:      $link_path -> $target"
      return 0
    fi
    rm "$link_path"
  elif [ -e "$link_path" ]; then
    local backup="${link_path}.pre-sync-adapters.bak"
    if [ -e "$backup" ]; then
      echo "WARN:    $link_path exists (not our symlink) and $backup already exists - resolve manually" >&2
      status=1
      return 0
    fi
    mv "$link_path" "$backup"
    echo "backed up: $link_path -> $backup"
  fi
  ln -s "$target" "$link_path"
  echo "linked:  $link_path -> $target"
}

gen_claude_agent() {
  local name="$1" tools="$2"
  local src="$HUB/agents/$name.md"
  local dest="$HUB/adapters/claude/agents/$name.md"
  local description body
  description="$(sed -n 's/^description: //p' "$src" | head -1)"
  body="$(awk 'BEGIN{n=0} /^---$/{n++; next} n>=2{print}' "$src")"
  {
    echo "---"
    echo "name: $name"
    echo "description: $description"
    if [ -n "$tools" ]; then
      echo "tools: $tools"
    fi
    echo "---"
    echo
    echo "<!-- GENERATED by bin/sync-adapters.sh from agents/$name.md - edit the source, not this file -->"
    printf '%s\n' "$body"
  } > "$dest"
  echo "generated: $dest"
}

echo "== Generating Claude Code agent adapters =="
mkdir -p "$HUB/adapters/claude/agents"
gen_claude_agent planner "Read, Grep, Glob, Bash"
gen_claude_agent reviewer ""
gen_claude_agent tester ""
gen_claude_agent maintainer ""
# build.md is OpenCode's primary agent, not a subagent - no Claude Code adapter.

echo
echo "== Wiring Claude Code (~/.claude) =="
link "$HUB/instructions/RULES.md" "$CLAUDE_DIR/CLAUDE.md"
link "$HUB/commands" "$CLAUDE_DIR/commands"
link "$HUB/adapters/claude/agents" "$CLAUDE_DIR/agents"
for s in $SKILLS; do
  link "$HUB/skills/$s" "$CLAUDE_DIR/skills/$s"
done

echo
echo "== Wiring Codex CLI (~/.codex, ~/.agents) =="
link "$HUB/instructions/RULES.md" "$CODEX_DIR/AGENTS.md"
link "$HUB/commands" "$CODEX_DIR/prompts"
for s in $SKILLS; do
  link "$HUB/skills/$s" "$AGENTS_SKILLS_DIR/$s"
done

echo
echo "== Verifying =="
broken=0
for d in "$CLAUDE_DIR" "$CODEX_DIR" "$AGENTS_SKILLS_DIR"; do
  [ -d "$d" ] || continue
  while IFS= read -r -d '' link_path; do
    if [ ! -e "$link_path" ]; then
      echo "BROKEN: $link_path" >&2
      broken=1
    fi
  done < <(find "$d" -maxdepth 3 -type l -print0 2>/dev/null)
done

if [ "$broken" -eq 1 ]; then
  echo "sync-adapters: one or more symlinks are broken" >&2
  exit 1
fi
if [ "$status" -ne 0 ]; then
  echo "sync-adapters: completed with warnings - see above" >&2
  exit 1
fi
echo "sync-adapters: OK"
